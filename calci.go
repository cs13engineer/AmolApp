package calci

//will add integers
func AddInt(i int, j int)int{
	return(i+j)
}

//will add float32
func AddFloat(a float32, b float32)float32{
	return(a+b)
}

//will subtract integers
func SubInt(c int, d int)int{
	return(c-d)
}

//will subtract float32
func SubFloat(e float32, f float32)float32{
	return(e-f)
}

//will multiply integers
func MulInt(g int, h int)int{
	return(g*h)
}

//will multiply float32
func MulFloat(k float32, l float32)float32{
	return(k*l)
}

//will divide integers
func DivInt(m int, n int)int{
	return(m/n)
}

//will divide the Float32 type
func DivFloat(o float32, p float32)float32{
	return(o/p)
}


